package com.example.demo.models;

public class LocalToken {
	private String token;

	public LocalToken() {
		super();
	}

	public LocalToken(String token) {
		super();
		this.token = token;
	}

	public String getToken() {
		return token;
	}

	public void setToken(String token) {
		this.token = token;
	}
	
}
